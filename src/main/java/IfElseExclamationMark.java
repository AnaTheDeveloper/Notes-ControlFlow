public class IfElseExclamationMark {

  //Exclamation mark means not

  public String isItRaining(boolean isItRaining) {


    if (!isItRaining) {

      //changes it to default false instead of true

      return "It is not raining";


    } else {
      return "It is raining stay inside";
    }



  }


}

